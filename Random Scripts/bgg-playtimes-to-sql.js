#!/usr/bin/env node
// bgg-playtimes-to-sql.js
//
// Reads bgg-playtimes.json and writes a SQL UPDATE script to import
// bggId, minTime, and maxTime into the Game table.
//
// Usage:
//   node bgg-playtimes-to-sql.js              (reads bgg-playtimes.json)
//   node bgg-playtimes-to-sql.js path/to/bgg-playtimes.json

import fs from 'fs';

const inputFile = process.argv[2] ?? 'bgg-playtimes.json';
const outputFile = 'import-bgg-playtimes.sql';

const records = JSON.parse(fs.readFileSync(inputFile, 'utf8'));
const valid = records.filter(r => r.bggId != null || r.minTime != null || r.maxTime != null);

if (!valid.length) {
    console.error('No records with BGG data found in input file.');
    process.exit(1);
}

const valueRows = valid.map(r => {
    const bggId   = r.bggId   != null ? r.bggId   : 'NULL';
    const minTime = r.minTime != null ? r.minTime : 'NULL';
    const maxTime = r.maxTime != null ? r.maxTime : 'NULL';
    return `    (${r.gameId}, ${bggId}, ${minTime}, ${maxTime})`;
});

const sql = `-- Generated by bgg-playtimes-to-sql.js from ${inputFile}
-- Updates Game records with bggId, minTime, maxTime, and lastBGGSync from BGG.
-- Review before running. Skips records where no BGG data was found.

UPDATE "Game" g
SET
    "bggId"       = v."bggId"::int,
    "minTime"     = v."minTime"::int,
    "maxTime"     = v."maxTime"::int,
    "lastBGGSync" = NOW()
FROM (VALUES
${valueRows.join(',\n')}
) AS v("gameId", "bggId", "minTime", "maxTime")
WHERE g.id = v."gameId"::int;
`;

fs.writeFileSync(outputFile, sql);
console.log(`Written ${valid.length} UPDATE rows to ${outputFile}`);
if (records.length !== valid.length) {
    console.log(`Skipped ${records.length - valid.length} record(s) with no BGG data.`);
}
